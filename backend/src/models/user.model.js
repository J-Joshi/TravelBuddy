import mongoose, { ModifiedPathsSnapshot } from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: [true, "password is required"], //custom message when required field is false;
    },
  },
  { timestamps: true }
);

export const User = mongoose.model("User", userSchema);
